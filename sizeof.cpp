#include<iostream>
using namespace std;

int main() {
	cout << "The size of integer datatype is " << sizeof(int) << " bytes" << endl;
	cout << "The size of double datatype is " << sizeof(double) << " bytes" << endl;
	cout << "The size of float datatype is " << sizeof(float) << " bytes" << endl;
	cout << "The size of char datatype is " << sizeof(char) << " bytes" << endl;
	return 0;
}